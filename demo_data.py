def build_demo_hypotheses(kpi_text: str, constraints_text: str) -> list[dict[str, Any]]:
    search_text = f"{kpi_text} {constraints_text}".lower()
    ranked = []
    for index, template in enumerate(DEMO_HYPOTHESIS_LIBRARY):
        score = sum(1 for keyword in template["keywords"] if keyword in search_text)
        ranked.append((score, -index, template))

    ranked.sort(reverse=True)
    selected = [template for _, _, template in ranked[:5]]
    return [_with_aliases(item) for item in selected]


