    enriched["why"] = hypothesis["reason"]
    enriched["resources"] = list(hypothesis["required_resources"])
    enriched["validation_plan"] = list(hypothesis["verification_plan"])
    enriched["sources"] = list(hypothesis["evidence"])
    return enriched


