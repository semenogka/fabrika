def build_demo_final_report(hypotheses: list[dict[str, Any]], review: Any) -> str:
    review_payload = review
    if isinstance(review, str):
        review_payload = json.loads(review)

    ranking_by_title = {
        item["title"]: item for item in review_payload.get("ranking", [])
    }
    sections = []

    for index, hypothesis in enumerate(hypotheses, start=1):
        ranking = ranking_by_title.get(hypothesis["title"], {})
        sources = "; ".join(
            f"{item.get('source')} (page {item.get('page')})"
            for item in hypothesis.get("evidence", [])
        ) or "не указаны"
        resources = "\n".join(f"- {item}" for item in hypothesis.get("required_resources", []))
        plan = "\n".join(f"1. {item}" for item in hypothesis.get("verification_plan", []))
        comments = " ".join(ranking.get("comments", [])) or "Комментариев нет."
        recommendation = ranking.get("recommendation", "revise")
        total_score = ranking.get("total_score", 70)
        risk_line = (
            "Основные риски: переносимость результата на промышленное питание, колебания минералогии,"
            " необходимость уточнить окно режимов."
        )

        sections.append(f"**Гипотеза {index}: {hypothesis['title']}**")
        sections.append(f"1. Краткое описание: {hypothesis['description']}")
        sections.append(f"2. Почему появилась гипотеза: {hypothesis['reason']}")
        sections.append(f"3. Используемый механизм: {hypothesis['mechanism']}")
        sections.append(f"4. Ссылки на источники: {sources}")
        sections.append(f"5. Ограничения и противоречия: {comments}")
        sections.append(f"6. Учтённые ограничения: recommendation={recommendation}, priority={hypothesis['priority']}")
        sections.append(f"7. Нужные ресурсы:\n{resources}")
        sections.append(f"8. План проверки:\n{plan}")
        sections.append(f"9. Возможные риски: {risk_line}")
        sections.append(f"10. Ожидаемый эффект: {hypothesis['expected_effect']}")
        sections.append(f"11. Итоговый рейтинг: {total_score}/100")
        sections.append("")

    return "\n".join(sections).strip()
